SELECT c.ID REF_ID_PER,
        c.SURNAME SNAME,
        c.FIRSTNAME NAME,
        c.LASTNAME MIDDLE_NAM,
        c.BIRTHDATE DATE_BIRTH,
        C.ENP,
        c.CONTACTS,
        C.REGISTRATION_ADDR,
        c.RESIDENTIAL_ADDR,
        c.REF_ID_HOS REF_ID_HOS,
        trunc(to_date(:DATE_REESTR,'dd.mm.yyyy')) DATA_REEST,
        c.DATA_ATTAC DATA_ATTAC,
        c.NOTES NOTES,
        c.FID_PERSON FID_PERSON,
        c.SP_MO SP_MO,
        c.PRIB_ID PRIB_ID,
        c.PODR PODR,
        c.REF_ID_DEN REF_ID_DEN,
        c.DATA_DENT DATA_DENT,
        c.NOTES_DENT NOTES_DENT,
        c.FID_DENT_S FID_DENT_S,
        c.SP_MO_DENT SP_MO_DENT,
        c.REF_ID_GIN REF_ID_GIN,
        c.DATA_GINE DATA_GINE,
        c.NOTES_GIN NOTES_GIN,
        c.FID_GINE_S FID_GINE_S,
        c.SP_MO_GINE SP_MO_GINE
  FROM REGISTATIONS_GIN c
 WHERE COALESCE(C.REF_ID_HOS,C.REF_ID_DEN,C.REF_ID_GIN) IS NOT null